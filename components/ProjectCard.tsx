function GenericIcon({ label }: { label: string }) {
  return (
    <span className="text-[9px] font-bold text-blue-glow/70 leading-none select-none">
      {label.slice(0, 2).toUpperCase()}
    </span>
  );
}

function StackIcons({ stack }: { stack: string[] }) {
  const visible = stack.slice(0, 5);
  const extra = stack.length - 5;
  return (
    <div>
      <p className="text-[10px] font-semibold text-text-dim uppercase tracking-widest mb-3">
        Tech Stack
      </p>
      <div className="flex items-center gap-2 flex-wrap">
        {visible.map((tech) => {
          const icon = stackIcons[tech];
          return (
            <div
              key={tech}
              title={icon?.label ?? tech}
              className="stack-icon"
            >
              {icon ? icon.svg : <GenericIcon label={tech} />}
            </div>
          );
        })}
        {extra > 0 && (
          <div className="stack-icon">
            <span className="text-[9px] font-bold text-text-dim">+{extra}</span>
          </div>
        )}
      </div>
    </div>
  );
}

